//4.C Program to Multiply two Floating Point Numbers
#include<stdio.h>
void main()
{
  float a,b;
  printf("Enter two floating point numbers");
  //Get the input for two floating numbers
  scanf("%f %f",&a,&b); 
  //Multiplyy two floating point numbers
  printf("The product of the two floating point numbers is %f",a*b);
}

